/// Thanks to build.rs.
const OUT_DIR: &str = env!("OUT_DIR");

static OUT_DIR_LOWERCASE: LazyLock<String> = LazyLock::new(|| {
    let s = OUT_DIR.to_owned();
    to_upper_lower_case(s, false)
});
static OUT_DIR_UPPERCASE: LazyLock<String> = LazyLock::new(|| {
    let s = OUT_DIR.to_owned();
    to_upper_lower_case(s, true)
});

static RND_PART_LOWERCASE: LazyLock<String> = LazyLock::new(|| {
    let s = file::existing_rnd_part(OUT_DIR)
        .unwrap()
        .expect("Expecting a file generated by build.rs.");
    to_upper_lower_case(s, false)
});
static RND_PART_UPPERCASE: LazyLock<String> = LazyLock::new(|| {
    let s = (*RND_PART_LOWERCASE).clone();
    to_upper_lower_case(s, true)
});

fn to_upper_lower_case(mut s: String, make_uppercase: bool) -> String {
    if s.is_ascii() {
        if make_uppercase {
            s.make_ascii_uppercase();
        } else {
            s.make_ascii_lowercase();
        }
    } else {
        // These create a new String - that's OK.
        s = if make_uppercase {
            s.to_uppercase()
        } else {
            s.to_lowercase()
        };
    };
    s
}

struct LocalFilePath {
    local_path: PathBuf,
    make_uppercase: bool,
}

struct StrPathIdentSanitizer<'a> {}

// Can't implement
// - `impl From<&LocalFilePath> for Option<&str>`, or
// - `impl Into<Option<&str>> for &LocalFilePath`
//
// because their lifetimes would have to be independent.
impl LocalFilePath {
    /// Get subpath, excluding any part shared with to [OUT_DIR].
    fn into_relativish_str(&self) -> Option<impl AsRef<str>> {
        if let Some(s) = self.local_path.to_str() {
            let s = if self.make_uppercase {
                s.to_uppercase()
            } else {
                s.to_lowercase()
            };

            let out_dir = if self.make_uppercase {
                &(*OUT_DIR_UPPERCASE)[..]
            } else {
                &(*OUT_DIR_LOWERCASE)[..]
            };

            let len = s.len().min(out_dir.len());
            let mut i = 0;
            while i < len {
                if s.as_bytes()[i] != out_dir.as_bytes()[i] {
                    break;
                }
                i += 1;
            }
            assert!(
                i < s.len(),
                "The macro crate that uses crate 'private' is at {s}, which should not be a subset of OUT_DIR: {OUT_DIR}"
            );
            return Some(OwnedStringSlice::new_starting_from(s, i));
        } else {
            None
        }
    }

    // Just a subset of
    // https://rust-lang.github.io/rfcs/2457-non-ascii-idents.html#reference-level-explanation
    // (which refers to https://www.unicode.org/reports/tr31/).
    fn sanitize_as_ident(&self) -> Option<impl Iterator<Item = char>> {
        if let Some(path) = self.into_relativish_str() {
            let path = path.as_ref();
            let mut first_char = true;

            let mut filtered = path.chars().filter_map(|c| {
                if first_char {
                    if c.is_alphabetic() {
                        first_char = false;
                        Some(c)
                    } else {
                        // Let's _not_ have the first character an underscore
                        None
                    }
                } else {
                    if c.is_alphanumeric() {
                        Some(c)
                    } else {
                        Some('_')
                    }
                }
            });
            Some(filtered)
        } else {
            None
        }
    }
}

fn local_file_path(span: Span, make_uppercase: bool) -> Option<LocalFilePath> {
    if let Some(mut local_path) = span.local_file() {
        let ostr = local_path.as_mut_os_string();
        if make_uppercase {
            ostr.make_ascii_uppercase();
        } else {
            ostr.make_ascii_lowercase();
        }
        Some(LocalFilePath {
            local_path,
            make_uppercase,
        })
    } else {
        None
    }
}

// Compatible with Rust non-ASCII identifiers
// (https://rust-lang.github.io/rfcs/2457-non-ascii-idents.html).
fn var_or_const_or_static_name(given_name: &Ident, make_uppercase: bool) -> Ident {
    let span = given_name.span();

    let path_based_prefix = {
        // Either of the following two approaches leaves extra spaces around :: path separators:
        //
        // let mut path_based_prefix = macro_crate_and_path.into_token_stream().to_string();
        //
        // let mut path_based_prefix = quote!{ #macro_crate_and_path }.to_string();
        let iter = macro_crate_and_path.segments.iter();
        let mut parts = Vec::with_capacity(2 * iter.len());
        let mut total_len = 0;
        iter.for_each(|seg| {
            let ident = seg.ident.to_string();
            total_len += ident.len() + 1;
            parts.push(ident);
        });

        let mut path_based_prefix = String::with_capacity(total_len);
        path_based_prefix.extend(
            parts
                .iter()
                .map(|s| iter::once(&s[..]).chain(iter::once("_")))
                .flatten(),
        );

        /*if true { panic!("{}", path_based_prefix);}*/
        to_upper_lower_case(path_based_prefix, make_uppercase)
    };

    let private_ = if make_uppercase {
        "PRIVATE_"
    } else {
        "private_"
    };

    let given_name = given_name.to_string();

    let rnd_part = if make_uppercase {
        &*RND_PART_UPPERCASE
    } else {
        &*RND_PART_LOWERCASE
    };

    // Intentionally putting the random part at the end, to make errors/warnings more
    // obvious/searchable.
    let full_name = format!("{path_based_prefix}{private_}{given_name}_{rnd_part}");

    Ident::new(&full_name, span)
}

