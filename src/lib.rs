fn def_const_or_static_grammar(
    input: TokenStream,
    which: ItemChoice,
    direct: bool,
) -> MacroStreamResult {
    assert!(which == ItemChoice::Const || which == ItemChoice::Static);
    rules!(input => {
        ( $short_name:ident:$ty:ty = $value:expr ) => {
            def_const_static(which, short_name, ty, value, direct)
        }
    })
    .into()
}
