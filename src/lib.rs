fn at_grammar(input: TokenStream, which: ConstStaticLetMut) -> MacroStreamResult {
    Ok(rules!(input => {
        ( $short_name:ident) => {
            at_impl(&short_name, None, which.requires_type_and_value_and_make_uppercase())
        }
        ( $short_name:ident@$path:path) => {
            at_impl(&short_name, Some(&path), which.requires_type_and_value_and_make_uppercase())
        }
    })
    .into())
}

fn at_direct_grammar(input: TokenStream) -> MacroStreamResult {
    Ok(rules!(input => {
        ( $short_name:ident, $full_name:ident, $tt:tt) => {
            let span = tt.span();
            // @TODO verify that full_name = short_name + tt.span
            quote_spanned! {span=>
                #full_name
            }
        }
    })
    .into())
}

/// Access choice for a var/value. For now (and hopefully forever) we ignore `static mut`.
#[derive(Clone, Copy, PartialEq, Eq)]
enum ConstStaticLetMut {
    CONST,
    STATIC,
    LET,
    MUT,
}
impl ConstStaticLetMut {
    /// One of: `let`, `let mut`, `const`, `static`.
    pub fn keywords(&self) -> &'static str {
        match self {
            Self::CONST => "const",
            Self::STATIC => "static",
            Self::LET => "let",
            Self::MUT => "let mut",
        }
    }
    pub fn requires_type_and_value_and_make_uppercase(&self) -> bool {
        self == &Self::CONST || self == &Self::STATIC
    }
}

fn def_const_static_let_mut(
    which: ConstStaticLetMut,
    short_name: &Ident,
    path: Option<&Path>,
    ty: Option<&Type>,
    value: Option<&Expr>,
    direct: bool,
) -> MacroStreamResult {
    assert!(!which.requires_type_and_value_and_make_uppercase() || ty.is_some());
    assert!(!which.requires_type_and_value_and_make_uppercase() || value.is_some());

    let full_name = var_or_const_or_static_name(
        path,
        short_name,
        which.requires_type_and_value_and_make_uppercase(),
    );

    let span = short_name.span();

    let const_static_let_mut_part = TokenStream::from_str(which.keywords())
        .map_err_to()
        .map_macro_err()
        .span_err(span)?;

    let type_part = if let Some(ty) = ty {
        quote_spanned! {span=>
            : #ty
        }
    } else {
        TokenStream::new()
    };
    let assign_part = if let Some(value) = value {
        quote_spanned! {span=>
            = #value
        }
    } else {
        TokenStream::new()
    };
    let direct_part = if direct {
        let doc = format!("(private) {short_name} {type_part}");
        // #[doc = #doc] works to generate tooltip/mouseover with rust-analyzer:
        quote_spanned! {span=>
            #[doc = #doc]
            macro_rules! #short_name {
                // @TODO require an input/parameter ----> span check
                ($tt:tt) => {
                    ::private::at_direct!(#short_name, #full_name, $tt)
                    //#full_name
                }
            }
        }
    } else {
        TokenStream::new()
    };
    Ok(quote_spanned! {span=>
        #const_static_let_mut_part #full_name #type_part #assign_part;
        #direct_part
    })
}

fn at_impl(short_name: &Ident, path: Option<&Path>, make_uppercase: bool) -> TokenStream {
    let full_name = var_or_const_or_static_name(path, short_name, make_uppercase);
    let span = short_name.span();
    quote_spanned! {span=>
        #full_name
    }
}
